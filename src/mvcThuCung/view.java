    public void fillTable(List<Model> list) {
        defaultTableModel = (DefaultTableModel) tblDanhSach.getModel();
        defaultTableModel.setRowCount(0);
        for (Model model : list) {
            defaultTableModel.addRow(model.toRowTable());
        }
    }

    public void fillData(int index) {
        Model model = list.get(index);
        txtMa.setText(model.getMa());
        txtTen.setText(model.getTen());
        txtChieuCao.setText(String.valueOf(model.getChieuCao()));
        txtCannang.setText(String.valueOf(model.getCanNang()));
        if (model.isGioiTinh()) {
            rdoDuc.setSelected(true);
        } else {
            rdoCai.setSelected(true);
        }

    }

